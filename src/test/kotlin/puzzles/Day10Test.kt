package puzzles

import org.assertj.core.api.Assertions.assertThat
import org.junit.jupiter.api.Test

internal class Day10Test {

    @Test
    fun part1() {
        val input1 = """
            .#..#
            .....
            #####
            ....#
            ...##
            """.trimIndent().split("\n")
        assertThat(Day10(input1).part1()).isEqualTo(8)

        val input2 = """
            ......#.#.
            #..#.#....
            ..#######.
            .#.#.###..
            .#..#.....
            ..#....#.#
            #..#....#.
            .##.#..###
            ##...#..#.
            .#....####
            """.trimIndent().split("\n")
        assertThat(Day10(input2).part1()).isEqualTo(33)

        val input3 = """
            #.#...#.#.
            .###....#.
            .#....#...
            ##.#.#.#.#
            ....#.#.#.
            .##..###.#
            ..#...##..
            ..##....##
            ......#...
            .####.###.
            """.trimIndent().split("\n")
        assertThat(Day10(input3).part1()).isEqualTo(35)

        val input4 = """
            .#..#..###
            ####.###.#
            ....###.#.
            ..###.##.#
            ##.##.#.#.
            ....###..#
            ..#.#..#.#
            #..#.#.###
            .##...##.#
            .....#.#..
            """.trimIndent().split("\n")
        assertThat(Day10(input4).part1()).isEqualTo(41)

        val input5 = """
            .#..##.###...#######
            ##.############..##.
            .#.######.########.#
            .###.#######.####.#.
            #####.##.#.##.###.##
            ..#####..#.#########
            ####################
            #.####....###.#.#.##
            ##.#################
            #####.##.###..####..
            ..######..##.#######
            ####.##.####...##..#
            .#####..#.######.###
            ##...#.##########...
            #.##########.#######
            .####.#.###.###.#.##
            ....##.##.###..#####
            .#.#.###########.###
            #.#.#.#####.####.###
            ###.##.####.##.#..##
            """.trimIndent().split("\n")
        assertThat(Day10(input5).part1()).isEqualTo(210)
    }

    @Test
